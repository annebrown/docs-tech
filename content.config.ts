import { defineContentConfig, defineCollection, z } from '@nuxt/content'

export default defineContentConfig({
  collections: {
    landing: defineCollection({
      type: 'page',
      source: 'landing.md'
    }),
    docsTech: defineCollection({
      type: 'page',
      source: {
        include: '**',
        exclude: ['landing.md'],
        prefix: '/docs-tech'
      },
      schema: z.object({
        links: z.array(z.object({
          label: z.string(),
          icon: z.string(),
          to: z.string(),
          target: z.string().optional()
        })).optional()
      })
    })
  }
})
